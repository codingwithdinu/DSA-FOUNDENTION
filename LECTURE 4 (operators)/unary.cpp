#include <iostream>
using namespace std;
int main(){
    //unary operations
    // + unary plus
    // - unary minus
    // ++ increment operator
    // -- decrement operator
    // ! logical complement operator


    // POST- INCREMENT OPERATOR (a++)
    // PRE- INCREMENT OPERATOR (++a)


    int a=4;
    cout<<(a++)<<endl;

    cout<<(a)<<endl;

    int b=4;

    cout<<(++b)<<endl;

    cout<<(b)<<endl;


    return 0;
}
