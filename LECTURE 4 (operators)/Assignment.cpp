#include <iostream>
using namespace std;
int main(){
    //assignment operations
    // = assign
    // += add and assign
    // -= subtract and assign
    // /= divide and assign
    // %= take modulus and assign
    // *= multiply and assign
    // //= take exponent and assign

    int num1;
    num1=6;
    cout<<(num1)<<endl;
    num1+=3;
    cout<<(num1)<<endl;
    num1-=3;
    cout<<(num1)<<endl;
    num1/=3;
    cout<<(num1)<<endl;
    num1%=3;
    cout<<(num1)<<endl;
    num1*=3;
    cout<<(num1)<<endl;
    return 0;
}